namespace XC\GDPR\View;

use XCart\Extender\Mapping\ListChild;

/**
 * GDPR settings warning
 *
 * @ListChild (list="crud.modulesettings.header", zone="admin", weight="100")
 */
class SettingsNote extends \XLite\View\AView
{
    /**
     * Return list of allowed targets
     *
     * @return array
     */
    public static function getAllowedTargets()
    {
        $list = parent::getAllowedTargets();

        $list[] = 'module';

        return $list;
    }

    /**
     * Return widget default template
     *
     * @return string
     */
    protected function getDefaultTemplate()
    {
        return 'modules/XC/GDPR/settings_note.twig';
    }

    /**
     * Check if widget is visible
     *
     * @return boolean
     */
    protected function isVisible()
    {
        return parent::isVisible()
            && $this->getModule() === 'XC-GDPR';
    }
}
